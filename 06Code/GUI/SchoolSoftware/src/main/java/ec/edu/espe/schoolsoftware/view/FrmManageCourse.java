    private void tblCoursesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCoursesMouseClicked

        int row = tblCourses.getSelectedRow();

        txtCourseId.setText(tblCourses.getValueAt(row, 0).toString());
        txtCourseName.setText(tblCourses.getValueAt(row, 1).toString());
        cmbTeacher.setSelectedItem(tblCourses.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tblCoursesMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String id = txtCourseId.getText();
        
        CourseController controller = new CourseController();
        controller.getCourseRepository().delete(id);

        JOptionPane.showMessageDialog(this, "Curso eliminado correctamente");

        loadCourses();
        clean();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        model.setRowCount(0);
        
        CourseController controller = new CourseController();
        
        if (rbtnId.isSelected()) {
            
            Course course = controller.getCourseRepository().findById(txtSearch.getText());
            
            if (course != null) {
                model.addRow(new Object[]{
                    course.getId(),
                    course.getName(),
                    course.getTeacherId()
                });
            }

        } else {

            ArrayList<Course> courses = controller.getCourseRepository().findByName(txtSearch.getText());
            for (Course course : courses) {

                model.addRow(new Object[]{
                    course.getId(),
                    course.getName(),
                    course.getTeacherId()
                });
            }

        }


    }//GEN-LAST:event_btnSearchActionPerformed

