    private void clean() {
        txtCourseId.setText("");
        txtCourseName.setText("");
        cmbTeacher.setSelectedItem(0);
    }

    private void loadCourses() {

        CourseController controller = new CourseController();
        ArrayList<Course> courses = controller.getCourseRepository().read();
        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Nombre");
        model.addColumn("Docente");

        for (Course course : courses) {
            model.addRow(new Object[]{
                course.getId(),
                course.getName(),
                course.getTeacherId()});
        }

        tblCourses.setModel(model);
    }

    private void loadTeachers() {

        TeacherController controller = new TeacherController();

        ArrayList<Teacher> teachers = controller.getTeacherRepository().read();

        cmbTeacher.removeAllItems();

        for (Teacher teacher : teachers) {
            cmbTeacher.addItem(teacher.getId() + " - " + teacher.getNames());
        }
    }

